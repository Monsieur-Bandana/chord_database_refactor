package chord.database.refactor;

public class Harmony{
    public int[] chord;
    public String german;
    public String english;
    public String roman;
    public String indonesian;

    public Harmony(int[] chord, String german, String english, String roman, String indonesian) {
        this.chord = chord;
        this.german = german;
        this.english = english;
        this.roman = roman;
        this.indonesian = indonesian;
    }
}
